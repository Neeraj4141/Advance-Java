package com.rays.marksheet;

public class MarksheetBean {

	private int rollno;
	private String name;
	private int phy;
	private int chy;
	private int maths;

	public int getRollno() {
		return rollno;
	}

	public void setRollno(int rollno) {
		this.rollno = rollno;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPhy() {
		return phy;
	}

	public void setPhy(int phy) {
		this.phy = phy;
	}

	public int getChy() {
		return chy;
	}

	public void setChy(int chy) {
		this.chy = chy;
	}

	public int getMaths() {
		return maths;
	}

	public void setMaths(int maths) {
		this.maths = maths;
	}

}
